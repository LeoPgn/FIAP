converte_oposto = lambda num : -num
converte_oposto(7)

converte_inverso = lambda x : 1/x
converte_inverso(3)

calcula_metade = lambda a : a/2
calcula_metade(6)

soma_quadrados = lambda x, y : x**2 + y**2
soma_quadrados(6,4)

exibe_pessoa = lambda nome, idade : print(f"{nome}, tem {idade} anos")
exibe_pessoa ("Ana", 21)

verifica_parImpar = lambda n : 0 if (n % 2 == 0) else 1
verifica_parImpar(4)

calcula_triplo = lambda b : b * 3
calcula_triplo(6)

